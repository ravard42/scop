#include "scop.h"

void		mouv_cam(t_env *e)
{
	if (e->c.mouv & 1)
		e->c.pos[0] += 0.1;
	if (e->c.mouv & 2)
		e->c.pos[0] -= 0.1;
	if (e->c.mouv & 4)
		e->c.pos[1] += 0.1;
	if (e->c.mouv & 8)
		e->c.pos[1] -= 0.1;
	if (e->c.mouv & 16)
		e->c.pos[2] -= 0.1;
	if (e->c.mouv & 32)
		e->c.pos[2] += 0.1;
}
