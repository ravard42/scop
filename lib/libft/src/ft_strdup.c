#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dup;
	int		i;

	if ((dup = (char *)malloc(sizeof(char) * (ft_strlen(str) + 1))) == NULL)
		return (NULL);
	i = 0;
	while (str[i])
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
